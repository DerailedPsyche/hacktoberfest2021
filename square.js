function setup() {  
       
    // Create Canvas of given size  
    createCanvas(300, 300);  
   
}  
   
function draw() {  
       
    background(220); 
       
    // Use color() function 
    let c = color('green'); 
   
    // Use fill() function to fill color 
    fill(c); 
     
    // Draw a square 
    square(50, 50, 200); 
     
}  
